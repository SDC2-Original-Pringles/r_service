CREATE TABLE IF NOT EXISTS reviews (
  id              int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  product_id      int references products(id),
  rating          int,
  date            bigint,
  summary         text,
  body            text,
  recommend       boolean,
  reported        boolean,
  reviewer_name   text,
  reviewer_email  text,
  response        text,
  helpfulness     int
);

