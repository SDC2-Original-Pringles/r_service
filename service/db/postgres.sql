DROP TABLE IF EXISTS products, reviews, reviews_photos, characteristics, characteristic_reviews CASCADE;

CREATE TABLE IF NOT EXISTS products (
  id            int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name          text,
  slogan        text,
  description   text,
  category      text,
  default_price int 
);

